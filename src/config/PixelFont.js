const CELL_W = 8;
const CELL_H = 8;
const COLS = 16;
const N_CHARS = 95;
const FIRST = 32;
const TEX_KEY = "pixel-tex";

/**
 * Generate the pixel font texture and register it as a Phaser BitmapFont.
 * Call once from BootScene.create() before any scene that uses the font.
 *
 * Uses a DOM canvas + textures.addImage() so pixels are uploaded to the GPU
 * as-is, without going through Phaser's WebGL rendering pipeline (which
 * mirrors the x-axis when rendering to an offscreen FBO via generateTexture).
 */
export function generatePixelFont(scene) {
  const rows = Math.ceil(N_CHARS / COLS);
  const texW = COLS * CELL_W;
  const texH = rows * CELL_H;

  const canvas = document.createElement("canvas");
  canvas.width = texW;
  canvas.height = texH;
  const ctx = canvas.getContext("2d");
  ctx.fillStyle = "#ffffff";
  for (let ci = 0; ci < N_CHARS; ci++) {
    const col = ci % COLS;
    const row = Math.floor(ci / COLS);
    const ox = col * CELL_W;
    const oy = row * CELL_H;
    const base = ci * CELL_H;
    for (let py = 0; py < CELL_H; py++) {
      let b = GLYPHS[base + py];
      for (let px = 0; px < CELL_W; px++, b <<= 1) {
        if (b & 0x80) ctx.fillRect(ox + (CELL_W - 1 - px), oy + py, 1, 1);
      }
    }
  }

  if (scene.textures.exists(TEX_KEY)) scene.textures.remove(TEX_KEY);
  scene.textures.addImage(TEX_KEY, canvas);

  const frame = scene.textures.get(TEX_KEY).get();

  const chars = {};
  for (let ci = 0; ci < N_CHARS; ci++) {
    const code = FIRST + ci;
    const col = ci % COLS;
    const row = Math.floor(ci / COLS);
    const gx = col * CELL_W;
    const gy = row * CELL_H;
    chars[code] = {
      x: gx,
      y: gy,
      width: CELL_W,
      height: CELL_H,
      centerX: Math.floor(CELL_W / 2),
      centerY: Math.floor(CELL_H / 2),
      xOffset: 0,
      yOffset: 0,
      xAdvance: CELL_W,
      data: {},
      kerning: {},
      u0: gx / texW,
      v0: gy / texH,
      u1: (gx + CELL_W) / texW,
      v1: (gy + CELL_H) / texH,
    };
  }

  scene.cache.bitmapFont.add(PIXEL_FONT, {
    data: { font: PIXEL_FONT, size: CELL_H, lineHeight: CELL_H + 2, chars },
    texture: TEX_KEY,
    frame,
  });
}
